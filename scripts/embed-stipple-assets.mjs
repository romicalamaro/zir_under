/**
 * Embeds stipple PNG + SVG for export (works on file:// where fetch fails).
 * Usage: node scripts/embed-stipple-assets.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const pngPath = join(root, "stipple-1780673179311.png");
const svgPath = join(root, "stipple-1780673179311.svg");
const outPath = join(root, "js/hopeStippleExportData.js");

const pngBase64 = readFileSync(pngPath).toString("base64");
const svgText = readFileSync(svgPath, "utf8");

const out =
  "/* Auto-generated by scripts/embed-stipple-assets.mjs — do not edit */\n" +
  "(function () {\n" +
  '  window.HOPE_STIPPLE_EXPORT_PNG_DATA_URI = "data:image/png;base64,' +
  pngBase64 +
  '";\n' +
  "  window.HOPE_STIPPLE_EXPORT_SVG_TEXT = " +
  JSON.stringify(svgText) +
  ";\n" +
  "})();\n";

writeFileSync(outPath, out);
console.log("Wrote", outPath, "(" + out.length + " bytes)");
